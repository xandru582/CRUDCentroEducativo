    public void cargaTabla() {
        DefaultTableModel modelo = (DefaultTableModel) jtAulas.getModel();
        try {
            modelo.setRowCount(0);
            List<Aula> aulas = aulaDaoImp.getAll();
            for (Aula aula : aulas) {
                Object[] lista = {
                    aula.getId(),
                    aula.getCodigo(),
                    aula.getDescripcion()
                };
                modelo.addRow(lista);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmTablaAula.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

