    private void jtAulasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtAulasMouseClicked
        if (evt.getClickCount() == 2) {
        int selectedRow = jtAulas.getSelectedRow();
        if (selectedRow != -1) {
            try {
                int idAula = (int) jtAulas.getValueAt(selectedRow, 0);
                String codigoAula = (String) jtAulas.getValueAt(selectedRow, 1);
                
                // Obtener el objeto Aula correspondiente al ID
                AulaDaoImp aulaDaoImp = AulaDaoImp.getInstance();
                Aula aula = aulaDaoImp.getById(idAula);
                
                // Crear el formulario frmAula y pasar el objeto Aula
                frmAula aulaForm = new frmAula();
                aulaForm.setAula(aula);
                aulaForm.miTablaInterna=this;
                aulaForm.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(frmTablaAula.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    }//GEN-LAST:event_jtAulasMouseClicked

