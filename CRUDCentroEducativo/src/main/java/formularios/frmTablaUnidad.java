    private void jtUnidadesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtUnidadesMouseClicked
        if (evt.getClickCount() == 2) {
            try {
                System.out.println("DOBLE CLIC");

                int idUnidad = Integer.parseInt(jtUnidades.getValueAt(jtUnidades.getSelectedRow(), 0).toString());

                UnidadDaoImp unidadDao = UnidadDaoImp.getInstance();
                Unidad unidad = unidadDao.getById(idUnidad);

                // Crear y mostrar el formulario de detalles de la unidad
                frmUnidad detallesUnidadForm = new frmUnidad();
                detallesUnidadForm.setUnidad(unidad);
                detallesUnidadForm.miTablaInterna = this;
                //detallesUnidadForm.cargaCampos(); // Agregar este método para cargar los campos del formulario
                detallesUnidadForm.setVisible(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al abrir los detalles de la unidad: " + ex.getMessage());
            }
        }


    }//GEN-LAST:event_jtUnidadesMouseClicked

