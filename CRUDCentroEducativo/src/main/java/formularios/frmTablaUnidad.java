    private void configTable() {
        String col[] = {"ID", "Código", "Nombre", "Observaciones", "ID Curso", "ID Tutor", "ID Aula"};
        modelo = new DefaultTableModel(col, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jtUnidades.setModel(modelo);
    }

    public void cargaTabla() {
        try {
            UnidadDaoImp unidadDao = UnidadDaoImp.getInstance();
            List<Unidad> unidades = unidadDao.getAll();

            for (Unidad unidad : unidades) {
                Object[] fila = {
                    unidad.getId(),
                    unidad.getCodigo(),
                    unidad.getNombre(),
                    unidad.getObservaciones(),
                    unidad.getIdCurso(),
                    unidad.getIdTutor(),
                    unidad.getIdAula()
                };
                modelo.addRow(fila);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar la tabla de unidades: " + ex.getMessage());
        }
    }


