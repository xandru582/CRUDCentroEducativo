    private void buscarAlumno() {
        String dni = txtDniAlumno.getText();
        AlumnoDaoImp alumnoDao = AlumnoDaoImp.getInstance();

        try {
            Alumno alumno = alumnoDao.getByDni(dni);
            miAlumnoInterno = alumno;
            if (alumno != null) {
                txtNombreAlumno.setText(alumno.getNombre());
                txtApellidoAlumno.setText(alumno.getApellido1());
                // Setear otros campos con la información necesaria del alumno
            } else {
                JOptionPane.showMessageDialog(this, "Alumno no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
                // Limpiar los campos de alumno
                txtNombreAlumno.setText("");
                txtApellidoAlumno.setText("");
                // Limpiar otros campos del alumno
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmAutorizaciones.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error al buscar el alumno", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

