    private void restablecerCampos() {
        txtDniAlumno.setText("");
        txtNombreAlumno.setText("");
        txtApellidoAlumno.setText("");
        txtDniAutorizado.setText("");
        txtNombreAutorizado.setText("");
        txtApellidoAutorizado.setText("");
    }

    private void agregarAutorizacion() {
        if (miAlumnoInterno == null || miAutorizadoInterno == null) {
            JOptionPane.showMessageDialog(this, "Por favor, busque un alumno y un autorizado", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        AutorizacionesDaoImp autorizacionesDao = AutorizacionesDaoImp.getInstance();

        try {
            Autorizaciones autorizacion = new Autorizaciones(miAlumnoInterno.getId(), miAutorizadoInterno.getId());
            int rowsAffected = autorizacionesDao.add(autorizacion);

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Autorización agregada correctamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                restablecerCampos();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo agregar la autorización", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al agregar la autorización", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void eliminarAutorizacion() {
        if (miAlumnoInterno == null || miAutorizadoInterno == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un alumno y un autorizado");
            return;
        }

        int idAlumno = miAlumnoInterno.getId();
        int idAutorizado = miAutorizadoInterno.getId();

        try {
            AutorizacionesDaoImp autorizacionesDao = AutorizacionesDaoImp.getInstance();
            Autorizaciones autorizacion = autorizacionesDao.getById(idAlumno, idAutorizado);

            if (autorizacion != null) {
                autorizacionesDao.delete(autorizacion);
                restablecerCampos();
                JOptionPane.showMessageDialog(this, "Autorización eliminada correctamente");
            } else {
                JOptionPane.showMessageDialog(this, "No existe una autorización para el alumno y autorizado seleccionados");
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmAutorizaciones.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error al eliminar la autorización", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


