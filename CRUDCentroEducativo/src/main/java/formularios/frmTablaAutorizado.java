    private void jtAutorizadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtAutorizadosMouseClicked
        if (evt.getClickCount() == 2) {
            try {
                System.out.println("DOBLE CLIC");

                // Obtener el valor de la columna 0 (ID)
                int idAutorizado = Integer.parseInt(jtAutorizados.getValueAt(jtAutorizados.getSelectedRow(), 0).toString());

                // Crear el formulario frmAutorizados y pasar el valor del ID
                frmAutorizados autorizadoForm = new frmAutorizados();
                AutorizadoDaoImp autorizadoDaoImp = AutorizadoDaoImp.getInstance();
                autorizadoForm.setAutorizado(autorizadoDaoImp.getById(idAutorizado));
                autorizadoForm.setTablaInterna(this);
                autorizadoForm.setVisible(true);
            } catch (Exception ex) {
                Logger.getLogger(frmTablaAutorizado.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_jtAutorizadosMouseClicked

