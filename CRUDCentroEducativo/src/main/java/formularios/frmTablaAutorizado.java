    private void configTabla() {
        String col[] = {"ID", "Nombre", "Apellido1", "Parentesco", "DNI"};
        DefaultTableModel modelo = new DefaultTableModel(col, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jtAutorizados.setModel(modelo);
    }

    public void cargaTabla() {
        DefaultTableModel modelo = (DefaultTableModel) jtAutorizados.getModel();
        AutorizadoDaoImp autorizadoDao = AutorizadoDaoImp.getInstance();

        try {
            modelo.setNumRows(0);
            for (Autorizado autorizado : autorizadoDao.getAll()) {
                Object[] lista = {
                    autorizado.getId(),
                    autorizado.getNombre(),
                    autorizado.getApellido1(),
                    autorizado.getParentesco(),
                    autorizado.getDni()
                };
                modelo.addRow(lista);
            }
        } catch (SQLException e) {
            // Manejar cualquier excepciÃ³n que ocurra durante la carga de datos
            e.printStackTrace();
        }
    }

